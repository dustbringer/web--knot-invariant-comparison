function hasInv(name: string, inv: string): boolean {
  return name.split("+").filter((s) => s === inv).length > 0;
  // return new RegExp(`(^${inv}\\+)|(\\+${inv}\\+)|(\\+${inv}$)|(^${inv}$)`).test(
  //   name
  // );
}

function removeInv(name: string, inv: string): string {
  return name
    .split("+")
    .filter((s) => s !== inv)
    .join("+");
}

const statsComb = (name: string): Array<number> => {
  if (name.split("+").length === 1) {
    const index = stats["unique"].columnsAbbr.indexOf(name);
    if (index < 0) {
      return range(0, stats["unique"].data.length).map(() => NaN);
    } else {
      return stats["unique"].data.map((row) => row[index + 1]);
    }
  }

  // Assuming there the input has at least two invariants
  if (hasInv(name, "HFK2") && hasInv(name, "A")) {
    return statsComb(removeInv(name, "A"));
  } else if (hasInv(name, "HFK2") && hasInv(name, "HFK2T1")) {
    return statsComb(removeInv(name, "HFK2T1"));
  } else if (hasInv(name, "HH") && hasInv(name, "H")) {
    return statsComb(removeInv(name, "H"));
  } else if (hasInv(name, "K") && hasInv(name, "J")) {
    return statsComb(removeInv(name, "J"));
  } else if (hasInv(name, "K") && hasInv(name, "KT1")) {
    return statsComb(removeInv(name, "KT1"));
  } else if (hasInv(name, "KR3") && hasInv(name, "A2")) {
    return statsComb(removeInv(name, "A2"));
  } else {
    return statsComb_[name];
  }
};

